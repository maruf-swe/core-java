package java_variables;

public class JavaVariables {
    public static void main(String[] args) {
        // String Variable. stores text
        String name = "Marufur Rahman";

        // int Variable. stores integers without decimal
        int number = 10;

        // double variable. stores floating point numbers
        double num = 1.1;

        // char variable. Store characters such as 'a' or 'B' with single quotes
        char a = 'a';

        // bolean variables is True or Fales
        boolean aa = true;
        boolean ff = false;

        // print variables value
        System.out.println(aa);
        System.out.println(number);

    }

}
