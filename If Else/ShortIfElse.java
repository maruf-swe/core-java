package if_else;

public class ShortIfElse {
    public static void main(String[] args) {

        /*
            Syntax of Short Hand If Else
            variable = (condition) ? expressionTrue :  expressionFalse;
        */

        int a = 10;
        String number = (a < 18) ? "Good Day" : "Good Night";
        System.out.println(number);
    }
}
