package javaoop.interfaces;

public class BasicsOfInterface {
/*
                Interfaces Overview:::::
An interface is a collection of abstract methods (methods without implementations) and constants. It defines a contract
  for any class that implements it. A class that implements an interface must provide an implementation for all the methods
     declared by the interface (unless the class is abstract).

                Characteristics of Interfaces:::
* No implementation: Methods in an interface do not have a body (prior to Java 8).After Java 8 interface have a body.
* Cannot be instantiated: You cannot create objects of an interface directly. Interfaces can only be implemented by classes.
* Implemented by classes: A class can implement multiple interfaces.
* Default access: Methods in an interface are implicitly public, and they must be overridden in the implementing class.
* Constants: Fields in an interface are implicitly public, static, and final.

               Why Use Interfaces?
* Achieving Abstraction: Interfaces provide a way to achieve abstraction by defining the method signatures without implementation.
                                after java its not fully abstract, its have default & static method.
* Multiple Inheritance: A class can implement multiple interfaces, thus enabling multiple inheritance of behavior.
* Loose Coupling: Interfaces allow for loose coupling between components, which makes the code more flexible and easier to maintain.
*/
}
