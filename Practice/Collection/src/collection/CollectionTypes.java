package collection;

public class CollectionTypes {

    /*

     Collection Type      Description                            Common Implementations          |
|-------------------|---------------------------------|-------------------------------------------------|
| List              | Ordered, allows duplicates      |         ArrayList, LinkedList, Vector, Stack    |
| Set               | No duplicates, unordered        |         HashSet, LinkedHashSet, TreeSet         |
| Queue             | FIFO order, used for processing |         PriorityQueue, LinkedList               |
| Deque             | Double-ended queue              |         ArrayDeque, LinkedList                  |
| Map               | Key-value pairs, unique keys    |         HashMap, TreeMap, LinkedHashMap         |

     */
}
