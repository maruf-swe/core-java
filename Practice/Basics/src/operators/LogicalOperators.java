package operators;

public class LogicalOperators {
    public static void main(String[] args) {
        int a = 10, b = 5;

        // 3. Logical Operators
        boolean x = true, y = false;
        System.out.println("Logical AND: " + (x && y));   // &&
        System.out.println("Logical OR: " + (x || y));    // ||
        System.out.println("Logical NOT: " + (!x));       // !
    }
}
