package operators;

public class UrinaryOperators {
    public static void main(String[] args) {
        // Java Operators
        int a = 10, b = 5;

        // 6. Unary Operators
        int f = 5;
        System.out.println("Unary plus: " + (+f));        // +
        System.out.println("Unary minus: " + (-f));       // -
        System.out.println("Pre-increment: " + (++f));    // ++
        System.out.println("Post-increment: " + (f++));   // ++
        System.out.println("Pre-decrement: " + (--f));    // --
        System.out.println("Post-decrement: " + (f--));   // --

    }
}
